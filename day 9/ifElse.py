# If else is a conditional statement that allows you to execute a block of code if a certain condition is true, and another block of code if the condition is false.

"""
Syntax:
if condition:
    # code to execute if condition is true
else:
    # code to execute if condition is false
"""

if 5>6:
    print("5 is greater than 6")
else:
    print("5 is not greater than 6")