# len function is used to find the length of a string, list, tuple, or any other iterable object.

name = "Pragyan"
no_of_letters = len(name)
print("The number of letters in the name is:", no_of_letters)

price = 456438938583
price_in_string = str(price) # "456438938583"
length_of_price = len(price_in_string)
print("The number of digits in the price is:", length_of_price)