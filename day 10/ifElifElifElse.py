# If elif elif else statement is used when we have more than two conditions to check. It allows us to check multiple conditions in a sequential manner. The syntax is as follows:
# if condition1:
#     # code to execute if condition1 is true
# elif condition2:
#     # code to execute if condition2 is true
# elif condition3:
#     # code to execute if condition3 is true
# else:
#     # code to execute if all conditions are false