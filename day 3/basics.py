# Syntax, Comments and Statement in python

# Syntax is the set of rules that defines how a Python program will be written and interpreted. It includes things like how to write variables, functions, loops, and other constructs. Example of syntax in Python are:
# 1. Variable assignment: x = 5
# 2. Function definition: def my_function():
# 3. If statement: if x > 0:
# 4. For loop: for i in range(5):
# 5. Print statement: print("Hello, World!")

# This is a comment in Python - Comments are used to explain the code and make it more readable. They are ignored by the Python interpreter and do not affect the execution of the program. In Python, comments can be created using the # symbol for single-line comments or triple quotes (''' or """) for multi-line comments.

