# Data types of python

# int
a = 10
print(type(a))

# float
b = 3.14
print(type(b))

# string
c = "Hello, World!"
print(type(c))

# boolean
d = True
print(type(d))

# list
e = [1, 2, 3, 4, 5]
print(type(e))

# tuple
f = (1, 2, 3, 4, 5)
print(type(f))

# set
g = {1, 2, 3, 4, 5}
print(type(g))

# dictionary
h = {
    "name": "Alice",
    "age": 30,
    "city": "New York"
}
print(type(h))

# NoneType
i = None
print(type(i))

# complex
j = 2 + 3j
print(type(j))

# bytes
k = b'Hello'
print(type(k))