# Operators in Python
# Arithmetic Operators
a = 10
b = 5
print("Addition:", a + b)        # 15
print("Subtraction:", a - b)     # 5
print("Multiplication:", a * b)  # 50
print("Division:", a / b)        # 2.0
print("Floor Division:", a // b) # 2
print("Modulus:", a % b)        # 0
print("Exponentiation:", a ** b) # 100000

# Comparison Operators
x = 10
y = 20
print("Equal to:", x == y)       # False
print("Not equal to:", x != y)   # True
print("Greater than:", x > y)    # False
print("Less than:", x < y)       # True
print("Greater than or equal to:", x >= y) # False
print("Less than or equal to:", x <= y)    # True

# Logical Operators
c = 10
d = 20
print("Logical AND:", c > 5 and d > 15)  # True
print("Logical OR:", c > 15 or d > 15)   # True
print("Logical NOT:", not (c > 5))       # False

# Assignment Operators
name = "Bishal"
age = 25
print("Name:", name)  # Bishal
print("Age:", age)    # 25